import * as React from 'react';
import { TableBody, TableHeader, Table } from '../Table';
import CopiableText from '../CopiableText';
import { Widget, WidgetHeader, WidgetBody } from '../widget';
import Text from '../Text';

interface WidgetProps {
	blocks: Record<string, string>[];
	title: string;
}

const BlockWidget: React.FC<WidgetProps> = props => {
	const { blocks, title } = props;

	return (
		<Widget>
			<WidgetHeader>
				<Text type={'h2'}>{title}</Text>
			</WidgetHeader>
			<WidgetBody>
				<Table>
					<TableHeader>
						<Text type={'th'}>Id</Text>
						<Text type={'th'}>Generated by</Text>
						<Text type={'th'}>Height</Text>
						<Text type={'th'}>Txs</Text>
					</TableHeader>
					<TableBody>
						{blocks.map(block => (
							<tr>
								<td>
									<CopiableText text={block.id} type={'td'}>
										{block.id}
									</CopiableText>
								</td>
								<td>
									<CopiableText text={block.generatedBy} type={'td'}>
										{block.generatedBy}
									</CopiableText>
								</td>
								<Text type={'td'} key={block.height}>
									{block.height}
								</Text>
								<Text type={'td'} key={block.txs}>
									{block.txs}
								</Text>
							</tr>
						))}
					</TableBody>
				</Table>
			</WidgetBody>
		</Widget>
	);
};

export default BlockWidget;
